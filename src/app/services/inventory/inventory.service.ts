import {Injectable} from '@angular/core';
import {InventoryComponent} from './inventory.component';
import {Info} from '../../components/btns/btn-plus/btn-plus.component';
import {MatDialog} from '@angular/material';

interface MyItems {
    name: string;
    src: string;
}


@Injectable()
export class InventoryService {
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Properties
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    myItems: MyItems[] = [];

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Constructor
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    constructor(public dialog: MatDialog) {

    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Methods
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    openInventory(data: Info): void {
        const dialogRef = this.dialog.open(InventoryComponent, {
            panelClass: ['full-size', 'inventory'],
            data: {data: data, items: this.myItems}
        });

        dialogRef.afterClosed().subscribe(result => {
            console.log('The dialog was closed');
        });
    }


}
